-- Create the hero table with first and last name
CREATE TABLE hero (
    id SERIAL PRIMARY KEY,
    firstName VARCHAR(50) NOT NULL,
    lastName VARCHAR(50) NOT NULL,
    strength INT CHECK (strength >= 1 AND strength <= 100),
    defense INT CHECK (defense >= 1 AND defense <= 100),
    hitpoints INT CHECK (hitpoints >= 10 AND hitpoints <= 500)
);

